package Lesson2;

public class Lesson2If {
    public static void main(String[] args) {
        int x = 40;

        if (x == 10) {
            System.out.println("This is 10");
        } else if (x == 20) {
            System.out.println("This is 20");
        } else if (x == 30) {
            System.out.println("This 30");
        } else {
            System.out.println("Other");
        }
    }
}
